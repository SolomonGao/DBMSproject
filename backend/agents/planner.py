class QueryStep(BaseModel):
    """A single data query step in the execution plan."""
    type: str = Field(..., description="Query type: dashboard, timeseries, geo, events, overview, top_events, hot_events, daily_brief, news_context, event_detail")
    params: Dict[str, Any] = Field(default_factory=dict, description="Parameters for the query")


class QueryContext(BaseModel):
    """Structured context extracted from natural language by the local router."""
    user_input: Optional[str] = Field(None, description="Original user query")
    location: Optional[str] = Field(None, description="Canonical location name, e.g. 'Washington DC', 'New York'")
    date_start: Optional[str] = Field(None, description="Start date in YYYY-MM-DD format")
    date_end: Optional[str] = Field(None, description="End date in YYYY-MM-DD format")
    event_type: Optional[str] = Field(None, description="One of: protest, conflict, cooperation")
    query_text: Optional[str] = Field(None, description="Free text search terms")
    intent_category: str = Field("search", description="search | detail | brief | overview | hot | off_topic")
    confidence: str = Field("high", description="high | medium | low")


class QueryPlan(BaseModel):
    """Structured plan generated by the Planner."""
    intent: str = Field(..., description="Brief description of user intent")
    thinking: Optional[str] = Field(None, description="AI reasoning for tool selection")
    time_range: Optional[Dict[str, str]] = Field(None, description="Resolved date range {start, end} if applicable")
    steps: List[QueryStep] = Field(..., description="Ordered list of queries to execute")
    visualizations: List[str] = Field(..., description="Recommended visualizations: stats_cards, timeline, map, heatmap, event_table, report")
    report_prompt: Optional[str] = Field(None, description="Optional prompt for AI report generation")
    notice: Optional[str] = Field(None, description="User-facing notice about query optimization")


class ReportResult(BaseModel):
    """AI-generated report based on query results."""
    summary: str = Field(..., description="Executive summary")
    key_findings: List[str] = Field(default_factory=list, description="Bullet point key findings")


