    _LOCATION_PATTERN = re.compile(
        r'\b(?:in|at|near|around|on)\s+([A-Z][A-Za-z\s,]+?)(?=\s+(?:in|at|near|around|on|during|for|from|to|of|with|this|last|'
        r'\d{4}|(?:jan|feb|mar|apr|may|jun|jul|aug|sep|oct|nov|dec)[a-z]*|\?|$)|$)',
        re.IGNORECASE,
    )

    _KNOWN_LOCATIONS = re.compile(
        r'\b(California|Texas|Florida|New York|Washington DC|Washington|Illinois|Pennsylvania|Ohio|'
        r'Georgia|North Carolina|Virginia|Michigan|Massachusetts|Arizona|Colorado|Minnesota|Wisconsin|'
        r'Maryland|Missouri|Tennessee|Indiana|Nevada|Oregon|Utah|Louisiana|Alabama|Kentucky|'
        r'Connecticut|Oklahoma|Iowa|Arkansas|Mississippi|Kansas|New Mexico|Nebraska|'
        r'West Virginia|Idaho|Hawaii|New Hampshire|Maine|Montana|Rhode Island|Delaware|'
        r'South Dakota|North Dakota|Alaska|Vermont|Wyoming|DC|'
        r'Los Angeles|Chicago|Houston|San Francisco|Seattle|Boston|Miami|Dallas|Philadelphia|'
        r'Atlanta|Denver|Phoenix|Detroit|Nashville|Portland|San Diego|Austin|New Orleans|'
        r'Las Vegas|Memphis|Louisville|Buffalo|Baltimore|Milwaukee|Tampa|Orlando|'
        r'Sacramento|Long Beach|Oakland|Canada|Mexico|United States|USA|US|'
        r'Ukraine|Gaza|Israel|China|Russia|UK|France|Germany|Japan|India|Australia|Brazil|'
        r'Europe|Asia|Africa|Middle East)\b',
        re.IGNORECASE,
    )

    # -----------------------------------------------------------------------
    # Fallback Entity Extraction (when Ollama is unavailable)
    # -----------------------------------------------------------------------

    def _fallback_extract_context(self, query: str) -> QueryContext:
        """Regex-based fallback extraction when Ollama router fails."""
        q = query.lower().strip()
        
        # Greeting check
        if len(q) < 3 or self._GREETING_PATTERNS.match(q):
            return QueryContext(intent_category="off_topic", confidence="low")
        
        # Event ID check
        evt_match = self._EVENT_ID_PATTERN.search(query.strip())
        if evt_match:
            return QueryContext(intent_category="detail", confidence="low")
        
        # Location
        location = None
        loc_match = self._LOCATION_PATTERN.search(query)
        if loc_match:
            location = loc_match.group(1).strip()
        else:
            loc_fallback = self._KNOWN_LOCATIONS.search(query)
            location = loc_fallback.group(1) if loc_fallback else None
        if location:
            location = re.sub(r'\s+(in|at|near|around|on|during|for|from|to|of|with)$', '', location, flags=re.IGNORECASE).strip()
            if location.lower() in ('dc', 'washington dc', 'washington, dc'):
                location = 'Washington DC'
        
        # Date range
        start_date, end_date = None, None
        nat_match = self._NATURAL_DATE_PATTERN.search(query)
        if nat_match:
            if nat_match.group(1):
                month_abbr = nat_match.group(1).lower()[:3]
                d = nat_match.group(2).zfill(2)
                y = nat_match.group(3)
            else:
                d = nat_match.group(4).zfill(2)
                month_abbr = nat_match.group(5).lower()[:3]
                y = nat_match.group(6)
            month_num = self._MONTH_MAP.get(month_abbr, '01')
            start_date = end_date = f"{y}-{month_num}-{d}"
        else:
            date_match = self._FULL_DATE_PATTERN.search(query)
            if date_match:
                if date_match.group(1):
                    y, m, d = date_match.group(1), date_match.group(2).zfill(2), date_match.group(3).zfill(2)
                elif date_match.group(4):
                    m, d, y = date_match.group(4).zfill(2), date_match.group(5).zfill(2), date_match.group(6)
                else:
                    y, m, d = date_match.group(7), date_match.group(8), date_match.group(9)
                start_date = end_date = f"{y}-{m}-{d}"
            else:
                my_match = self._MONTH_YEAR_PATTERN.search(query)
                if my_match:
                    month_abbr = my_match.group(1).lower()[:3]
                    year = my_match.group(2)
                    month_num = self._MONTH_MAP.get(month_abbr, '01')
                    start_date = f"{year}-{month_num}-01"
                    next_month = datetime.strptime(start_date, '%Y-%m-%d') + timedelta(days=32)
                    end_date = (next_month.replace(day=1) - timedelta(days=1)).strftime('%Y-%m-%d')
                elif self._TIME_MENTIONED.search(query):
                    start_date = DEFAULT_DATA_START
                    end_date = DEFAULT_DATA_END
        
        # Hot keywords
        if self._HOT_KEYWORDS.search(query):
            return QueryContext(
                location=location, date_start=start_date, date_end=end_date,
                intent_category="hot", confidence="low",
            )
        
        return QueryContext(
            location=location, date_start=start_date, date_end=end_date,
            intent_category="search", confidence="low",
        )

    # -----------------------------------------------------------------------
    # Rule-Based Planner (uses structured context from router)
    # -----------------------------------------------------------------------

    def _rule_based_plan(self, ctx: QueryContext) -> Optional[QueryPlan]:
        """Fast rule-based planner using structured context from Ollama router."""
        # Off-topic
        if ctx.intent_category == "off_topic":
            return QueryPlan(
                intent="off_topic",
                thinking="User input is a greeting or too short — no tools needed.",
                steps=[],
                visualizations=[],
                report_prompt=None,
            )

        # Event detail lookup
        if ctx.intent_category == "detail":
            # Try user_input first, then query_text (Ollama may put fingerprint there)
            search_text = (ctx.user_input or "") + " " + (ctx.query_text or "")
            evt_match = self._EVENT_ID_PATTERN.search(search_text)
            if evt_match:
                fingerprint = evt_match.group(1) or evt_match.group(2) or evt_match.group(0)
                # For EVT-IDs, extract numeric seed for similar_events
                if fingerprint.upper().startswith('EVT-'):
                    try:
                        seed_id = int(fingerprint.split('-')[-1])
                    except ValueError:
                        seed_id = None
                elif fingerprint.upper().startswith('US-'):
                    # Custom fingerprint: look up by fingerprint string, no similar_events seed
                    seed_id = None
                else:
                    seed_id = int(fingerprint)
                steps = [QueryStep(type="event_detail", params={"fingerprint": fingerprint})]
                if seed_id:
                    steps.append(QueryStep(type="similar_events", params={"seed_event_id": seed_id, "limit": 10}))
                return QueryPlan(
                    intent="event_detail_lookup",
                    thinking="User provided a specific event ID — retrieving event details + similar events.",
                    steps=steps,
                    visualizations=["event_table", "report"],
                    report_prompt=f"Summarize this specific GDELT event ({fingerprint}) and highlight related events.",
                )

        # Daily brief
        if ctx.intent_category == "brief":
            query_date = ctx.date_start or (datetime.now() - timedelta(days=1)).strftime('%Y-%m-%d')
            end_date = ctx.date_end or query_date

            # If location is provided, search events in that location instead of global daily stats
            if ctx.location:
                params: Dict[str, Any] = {
                    "limit": 20,
                    "start_date": query_date,
                    "end_date": end_date,
                    "location_hint": ctx.location,
                }
                if ctx.event_type:
                    params["event_type"] = ctx.event_type
                return QueryPlan(
                    intent=f"events_{ctx.location}_{query_date}",
                    thinking=f"User requested events in {ctx.location} on {query_date}. Searching for location-matched events.",
                    steps=[QueryStep(type="events", params=params)],
                    visualizations=["event_table", "report"],
                    report_prompt=f"Summarize what happened in {ctx.location} on {query_date}.",
                )

            # No location — return global daily summary
            return QueryPlan(
                intent="daily_brief",
                thinking=f"User requested daily brief for {query_date}.",
                steps=[QueryStep(type="daily_brief", params={"query_date": query_date})],
                visualizations=["stats_cards", "report"],
                report_prompt=f"Summarize the key events on {query_date}.",
            )

        # Hot / Top events
        if ctx.intent_category == "hot":
            is_single_day = ctx.date_start is not None and ctx.date_end is not None and ctx.date_start == ctx.date_end
            if is_single_day:
                params = {"query_date": ctx.date_start, "top_n": 10}
                if ctx.location:
                    params["region_filter"] = ctx.location
                return QueryPlan(
                    intent=f"hot_events_{ctx.location or 'global'}",
                    thinking=f"User requested hot events for {ctx.date_start}." + (f" Filtering by region: {ctx.location}." if ctx.location else ""),
                    steps=[QueryStep(type="hot_events", params=params)],
                    visualizations=["event_table", "report"],
                    report_prompt=f"Summarize the hottest events on {ctx.date_start}" + (f" in {ctx.location}" if ctx.location else "") + ".",
                )
            else:
                params = {
                    "start_date": ctx.date_start or DEFAULT_DATA_START,
                    "end_date": ctx.date_end or DEFAULT_DATA_END,
                    "top_n": 10,
                }
                if ctx.location:
                    params["region_filter"] = ctx.location
                if ctx.event_type:
                    params["event_type"] = ctx.event_type
                return QueryPlan(
                    intent=f"top_events_{ctx.event_type or 'all'}_{ctx.location or 'global'}",
                    thinking=f"User requested top events" + (f" ({ctx.event_type})" if ctx.event_type else "") + (f" in {ctx.location}" if ctx.location else "") + f" from {params['start_date']} to {params['end_date']}.",
                    steps=[QueryStep(type="top_events", params=params)],
                    visualizations=["event_table", "report"],
                    report_prompt=f"Summarize the top {ctx.event_type or ''} events" + (f" in {ctx.location}" if ctx.location else "") + f" from {params['start_date']} to {params['end_date']}.",
                )

        # Regional overview
        if ctx.intent_category == "overview" and ctx.location:
            params = {"region": ctx.location}
            if ctx.date_start and ctx.date_end:
                params["start_date"] = ctx.date_start
                params["end_date"] = ctx.date_end
            else:
                params["time_range"] = "week"
            return QueryPlan(
                intent=f"regional_overview_{ctx.location}",
                thinking=f"User requested regional overview for {ctx.location}." + (f" Time range: {ctx.date_start} to {ctx.date_end}." if ctx.date_start else " Default: last week."),
                steps=[QueryStep(type="regional_overview", params=params)],
                visualizations=["stats_cards", "event_table", "report"],
                report_prompt=f"Provide a situation overview for {ctx.location}" + (f" from {ctx.date_start} to {ctx.date_end}" if ctx.date_start else " over the last week") + ".",
            )

        # Default: events search
        params: Dict[str, Any] = {
            "limit": 50,
            "start_date": ctx.date_start or DEFAULT_DATA_START,
            "end_date": ctx.date_end or DEFAULT_DATA_END,
        }
        if ctx.location:
            params["location_hint"] = ctx.location
        if ctx.event_type:
            params["event_type"] = ctx.event_type
        if ctx.query_text:
            params["query"] = ctx.query_text
        
        intent_name = f"events_{ctx.event_type or 'all'}_{ctx.location or 'global'}"
        return QueryPlan(
            intent=intent_name,
            thinking="Event search based on extracted location, date, and event type.",
            steps=[QueryStep(type="events", params=params)],
            visualizations=["event_table", "report"],
            report_prompt=f"Summarize {ctx.event_type or 'events'} {f'in {ctx.location}' if ctx.location else ''} from {params['start_date']} to {params['end_date']}.",
        )

    # -----------------------------------------------------------------------
    # LLM Tool Planner
    # -----------------------------------------------------------------------

    async def _llm_plan(self, query: str, ctx: QueryContext) -> QueryPlan:
        """Use remote LLM for complex queries. Passes simplified context extracted by Ollama."""
        # Build simplified user intent from structured context
        parts = []
        if ctx.location:
            parts.append(f"Location: {ctx.location}")
        if ctx.date_start:
            if ctx.date_start == ctx.date_end:
                parts.append(f"Date: {ctx.date_start}")
            else:
                parts.append(f"Date range: {ctx.date_start} to {ctx.date_end}")
        if ctx.event_type:
            parts.append(f"Event type: {ctx.event_type}")
        if ctx.query_text:
            parts.append(f"Search terms: {ctx.query_text}")
        
        simplified = "; ".join(parts) if parts else query
        
        # Also attach original query as reference
        user_msg = f"Simplified user intent: {simplified}\n\nOriginal request: \"{query}\""
        
        # Add any extra context from Ollama
        context_parts = []
        if ctx.intent_category != "search":
            context_parts.append(f"Intent category: {ctx.intent_category}")
        context_str = "\n".join(context_parts) if context_parts else ""
        if context_str:
            user_msg += f"\n\nExtracted context:\n{context_str}"
        user_msg += "\n\nRespond with ONLY raw JSON:"

        messages = [
            SystemMessage(content=PLANNER_SYSTEM_PROMPT),
            HumanMessage(content=user_msg),
        ]

        try:
            response = await asyncio.wait_for(self.llm.ainvoke(messages), timeout=120.0)
            content = response.content if hasattr(response, "content") else str(response)
            print(f"[Planner] raw response length: {len(content)}", flush=True)

            raw = _extract_json(content)
            if raw is None:
                # Print full response for debugging (truncated if very long)
                debug_content = content if len(content) < 800 else content[:400] + "\n... [truncated]\n" + content[-200:]
                print(f"[Planner] JSON extraction failed. Raw response:\n{debug_content}", flush=True)
                raise ValueError("Could not extract valid JSON from LLM response")

            plan = _normalize_plan(raw)
            print(f"[Planner] generated plan: {plan.intent} with {len(plan.steps)} steps", flush=True)
            return plan

        except asyncio.TimeoutError:
            print(f"[Planner] LLM timeout after 120s, using fallback plan", flush=True)
            return self._fallback_plan(query)
        except Exception as e:
            print(f"[Planner] failed: {e}, using fallback plan", flush=True)
            return self._fallback_plan(query)

    def _fallback_plan(self, query: str) -> QueryPlan:
        """Fallback when LLM or router fails."""
        return QueryPlan(
            intent="fallback_search",
            thinking="Planner error. Falling back to top events search.",
            steps=[QueryStep(type="top_events", params={"top_n": 20, "start_date": DEFAULT_DATA_START, "end_date": DEFAULT_DATA_END})],
            visualizations=["event_table", "report"],
            report_prompt=f"Top events for: {query}",
            notice="Planner error. Showing top-ranked events across the full dataset (fast mode). Add a month or date for more precise results.",
        )

    # -----------------------------------------------------------------------
    # Main Entry Point
    # -----------------------------------------------------------------------

    async def plan(self, user_query: str) -> tuple[QueryPlan, List[Dict[str, Any]]]:
        """Generate a query plan from user input.
        
        Simple flow:
        1. Ollama extracts structured context (location, date, event_type, intent).
        2. Rule-based planner directly maps context to tool calls.
        3. Only truly ambiguous queries fall back to remote LLM.
        Returns (QueryPlan, phases).
        """
        import time
        phases: List[Dict[str, Any]] = []
        stripped = user_query.strip()

        # Fast path: greetings (avoid even calling router)
        if self._GREETING_PATTERNS.match(stripped) or len(stripped) < 3:
            print(f"[Planner] Fast off-topic detected for: '{stripped}'", flush=True)
            return QueryPlan(
                intent="off_topic",
                steps=[],
                visualizations=[],
                report_prompt=None,
            ), [{"name": "Intent Check", "status": "completed", "detail": "Greeting or too-short input detected — skipping AI.", "elapsed_ms": 0}]

        # Phase 1: Context Extraction (Ollama)
        t0 = time.time()
        ctx = await self.router.extract_context(stripped)
        ctx.user_input = stripped  # Preserve original query
        t_router = round((time.time() - t0) * 1000, 1)
        
        detail_parts = [f"intent='{ctx.intent_category}'"]
        if ctx.location:
            detail_parts.append(f"loc='{ctx.location}'")
        if ctx.date_start:
            detail_parts.append(f"date={ctx.date_start} to {ctx.date_end}")
        if ctx.event_type:
            detail_parts.append(f"type='{ctx.event_type}'")
        if ctx.query_text:
            detail_parts.append(f"text='{ctx.query_text}'")
        
        print(f"[Planner] Context: {', '.join(detail_parts)} | confidence={ctx.confidence} in {t_router}ms", flush=True)
        phases.append({
            "name": "Context Extraction",
            "status": "completed",
            "detail": f"Local Qwen2.5b extracted: {', '.join(detail_parts)} ({ctx.confidence} confidence)",
            "elapsed_ms": t_router,
        })

        if ctx.intent_category == "off_topic":
            return QueryPlan(
                intent="off_topic",
                steps=[],
                visualizations=[],
                report_prompt=None,
            ), phases

        # Phase 2: Rule-Based Plan Generation — direct tool mapping, no LLM for simple queries
        t0 = time.time()
        fast_plan = self._rule_based_plan(ctx)
        if fast_plan:
            t_plan = round((time.time() - t0) * 1000, 1)
            step_types = ' → '.join(s.type for s in fast_plan.steps)
            detail = f"Matched '{fast_plan.intent}' | Tools: {step_types}"
            print(f"[Planner] Rule-based plan: {fast_plan.intent} with {len(fast_plan.steps)} steps in {t_plan}ms", flush=True)
            phases.append({"name": "Rule-Based Planning", "status": "completed", "detail": detail, "elapsed_ms": t_plan})
            return fast_plan, phases
        
        # Fallback: remote LLM for truly ambiguous queries
        print(f"[Planner] Rule base couldn't match, falling back to remote LLM", flush=True)
        plan = await self._llm_plan(stripped, ctx)
        t_plan = round((time.time() - t0) * 1000, 1)
        step_types = ' → '.join(s.type for s in plan.steps)
        detail = f"Remote LLM generated plan | Tools: {step_types}"
        phases.append({"name": "AI Planning", "status": "completed", "detail": detail, "elapsed_ms": t_plan})
        return plan, phases


