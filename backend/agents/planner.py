class OllamaRouter:
    """Fast local intent router using qwen2.5:3b via Ollama.
    
    Routes queries to one of three categories:
    - rule_base: Simple lookups with explicit filters (time, location, event type, ID)
    - llm_tools: Complex queries requiring reasoning/comparison/synthesis
    - off_topic: Greetings, small talk, unrelated questions
    
    Tries multiple URLs in order (env var → host.docker.internal → localhost)
    to support both Docker and native environments.
    """

    MODEL = "qwen2.5:3b"

    def _get_urls(self) -> list[str]:
        """Return candidate Ollama URLs to try."""
        urls = []
        env_url = os.getenv("OLLAMA_URL")
        if env_url:
            urls.append(env_url)
        # Docker Desktop / host network
        urls.append("http://host.docker.internal:11434/api/generate")
        # Native / Linux Docker with host network
        urls.append("http://localhost:11434/api/generate")
        return urls

    SYSTEM_PROMPT = """You extract structured information from natural language queries for a GDELT event search system.

Extract and return ONLY this JSON format:
{
  "location": "canonical location name or null",
  "date_start": "YYYY-MM-DD or null",
  "date_end": "YYYY-MM-DD or null",
  "event_type": "protest|conflict|cooperation or null",
  "query_text": "free text search terms or null",
  "intent_category": "search|detail|brief|overview|hot|off_topic"
}

Date extraction rules:
- "May 1 2024" → "2024-05-01" to "2024-05-01"
- "Jan 2024" → "2024-01-01" to "2024-01-31"
- "Q1 2024" → "2024-01-01" to "2024-03-31"
- "last week" → previous 7 days from today
- "recently" → last 30 days
- "2024" → "2024-01-01" to "2024-12-31"
- null → "2024-01-01" to "2024-12-31"

Location canonicalization:
- "NYC", "NY", "New York City" → "New York"
- "DC", "Washington DC", "Washington, D.C." → "Washington DC"
- "LA" → "Los Angeles"
- "SF" → "San Francisco"
- "TX" → "Texas"

Event type mapping:
- "protest", "demonstration", "rally", "march" → "protest"
- "conflict", "war", "attack", "fight", "battle", "clash" → "conflict"
- "cooperation", "treaty", "agreement", "partnership" → "cooperation"

Intent categories:
- "search": general event search (most common)
- "detail": user provided event ID like EVT-2024-01-15-12345 — WHEN INPUT IS AN EVENT ID: set ONLY intent_category="detail" and query_text="the full EVT-... ID", ALL OTHER FIELDS MUST BE null
- "brief": "daily brief", "what happened on [date]"
- "overview": "situation in [region]", "what's happening in [region]"
- "hot": "hottest event", "top events", "most popular"
- "off_topic": greeting, thanks, small talk

All data is from 2024 only.
No other text. Just the JSON."""

    def __init__(self):
        self._client: Optional[httpx.AsyncClient] = None

    @property
    def client(self) -> httpx.AsyncClient:
        if self._client is None:
            self._client = httpx.AsyncClient(timeout=httpx.Timeout(10.0, connect=2.0))
        return self._client

    async def extract_context(self, query: str) -> QueryContext:
        """Extract structured context from natural language query.
        Tries each Ollama URL in order. Falls back to regex-based extraction on total failure.
        """
        prompt = f"{self.SYSTEM_PROMPT}\n\nQuery: \"{query}\"\n\nJSON:"
        last_error = None
        
        for url in self._get_urls():
            try:
                resp = await self.client.post(
                    url,
                    json={
                        "model": self.MODEL,
                        "prompt": prompt,
                        "stream": False,
                        "options": {"temperature": 0.0, "num_predict": 120},
                    },
                )
                resp.raise_for_status()
                data = resp.json()
                text = data.get("response", "").strip()
                
                raw = _extract_json(text)
                if raw is None:
                    continue
                
                location = raw.get("location")
                if location and location.lower() in ('null', 'none', 'n/a', ''):
                    location = None
                
                event_type = raw.get("event_type")
                if event_type and event_type.lower() in ('null', 'none', 'n/a', ''):
                    event_type = None
                
                query_text = raw.get("query_text")
                if query_text and query_text.lower() in ('null', 'none', 'n/a', ''):
                    query_text = None
                
                date_start = raw.get("date_start")
                date_end = raw.get("date_end")
                if date_start and date_start.lower() in ('null', 'none', 'n/a', ''):
                    date_start = None
                if date_end and date_end.lower() in ('null', 'none', 'n/a', ''):
                    date_end = None
                
                intent = raw.get("intent_category", "search")
                if intent not in ("search", "detail", "brief", "overview", "hot", "off_topic"):
                    intent = "search"
                
                # Post-process: if query_text looks like an event fingerprint, force intent=detail
                # and clear all other fields (fingerprint lookup does not need date/location filters)
                check_text = (query_text or "").strip()
                if not check_text:
                    check_text = user_input.strip()
                if check_text:
                    # Match EVT-YYYY-MM-DD-NNNNNNNNNN, US-YYYYMMDD-LOC-TYPE-NUM, or raw numeric ID
                    if re.match(r'^(EVT-\d{4}-\d{2}-\d{2}-\d+|US-\d{8}-[A-Z]+-[A-Z]+-\d+|\d{9,12})$', check_text, re.IGNORECASE):
                        intent = "detail"
                        location = None
                        date_start = None
                        date_end = None
                        event_type = None
                        # Put the fingerprint into query_text so planner can extract it
                        if not query_text:
                            query_text = check_text
                
                return QueryContext(
                    location=location,
                    date_start=date_start,
                    date_end=date_end,
                    event_type=event_type,
                    query_text=query_text,
                    intent_category=intent,
                    confidence="high",
                )
                
            except Exception as e:
                last_error = e
                continue
        
        print(f"[OllamaRouter] All URLs failed (last: {last_error}), falling back to regex extraction", flush=True)
        return self._fallback_extract_context(query)

    async def extract_context_fast(self, query: str) -> QueryContext:
        """Fast-path context extraction using regex only — no Ollama call.
        
        Used for queries with obvious structured filters (date, location, event_type)
        where we don't need LLM reasoning. Returns in < 5ms vs 2s for Ollama.
        """
        import time
        t0 = time.time()
        
        q_lower = query.lower()
        
        # Date extraction
        date_start, date_end = None, None
        
        # Full date: "Jan 25 2024", "January 25, 2024", "2024-01-25"
        m = re.search(r'(?:jan|feb|mar|apr|may|jun|jul|aug|sep|oct|nov|dec)[a-z]*\.?\s+(\d{1,2})(?:st|nd|rd|th)?[,\s]+(2024)', q_lower)
        if m:
            month_map = {'jan':1,'feb':2,'mar':3,'apr':4,'may':5,'jun':6,
                         'jul':7,'aug':8,'sep':9,'oct':10,'nov':11,'dec':12}
            month_abbr = q_lower[m.start():m.start()+3]
            month = month_map.get(month_abbr, 1)
            day = int(m.group(1))
            date_start = date_end = f"2024-{month:02d}-{day:02d}"
        else:
            m = re.search(r'(\d{4})-(\d{2})-(\d{2})', query)
            if m:
                date_start = date_end = m.group(0)
        
        # Month only: "feb 2024", "in february"
        if not date_start:
            m = re.search(r'(?:jan|feb|mar|apr|may|jun|jul|aug|sep|oct|nov|dec)[a-z]*', q_lower)
            if m:
                month_map = {'jan':1,'feb':2,'mar':3,'apr':4,'may':5,'jun':6,
                             'jul':7,'aug':8,'sep':9,'oct':10,'nov':11,'dec':12}
                month_abbr = q_lower[m.start():m.start()+3]
                month = month_map.get(month_abbr, 1)
                date_start = f"2024-{month:02d}-01"
                date_end = f"2024-{month:02d}-28"  # Simple, data is Jan-only mostly
        
        # Location extraction
        location = None
        loc_patterns = [
            (r'(?:near|in|around)\s+(?:the\s+)?white\s+house', 'Washington DC'),
            (r'(?:near|in|around)\s+(?:washington\s+dc?|washington,\s*d\.?c\.?)', 'Washington DC'),
            (r'(?:near|in|around)\s+(?:new\s+york\s+city?|nyc|new\s+york)', 'New York'),
            (r'(?:near|in|around)\s+(?:los\s+angeles|la)', 'Los Angeles'),
            (r'(?:near|in|around)\s+(?:san\s+francisco|sf)', 'San Francisco'),
            (r'(?:near|in|around)\s+texas', 'Texas'),
            (r'(?:near|in|around)\s+california', 'California'),
            (r'(?:near|in|around)\s+florida', 'Florida'),
        ]
        for pattern, canonical in loc_patterns:
            if re.search(pattern, q_lower):
                location = canonical
                break
        
        # Event type extraction
        event_type = None
        if 'protest' in q_lower or 'demonstration' in q_lower or 'rally' in q_lower:
            event_type = 'protest'
        elif 'conflict' in q_lower or 'war' in q_lower or 'attack' in q_lower:
            event_type = 'conflict'
        elif 'cooperation' in q_lower or 'treaty' in q_lower or 'agreement' in q_lower:
            event_type = 'cooperation'
        
        # Intent: brief if "what happened", otherwise search
        intent = 'search'
        if re.search(r'what\s+happened|daily\s+brief|what\s+was', q_lower):
            intent = 'brief'
        
        # Query text: strip out obvious filter words to keep only meaningful search terms
        query_text = query
        
        elapsed_ms = round((time.time() - t0) * 1000, 3)
        print(f"[OllamaRouter] Fast-path regex extraction: {elapsed_ms}ms | loc={location}, date={date_start}, type={event_type}, intent={intent}", flush=True)
        
        return QueryContext(
            location=location,
            date_start=date_start,
            date_end=date_end,
            event_type=event_type,
            query_text=query_text,
            intent_category=intent,
            confidence="high",
        )

    async def close(self):
        if self._client:
            await self._client.aclose()
            self._client = None


